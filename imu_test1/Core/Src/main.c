accelData_t GetAcceleration()
{
	accelData_t retVal;
	uint8_t sendByte;
	uint8_t rcvByte;
	HAL_GPIO_WritePin(MPU9250_CS_GPIO_Port, MPU9250_CS_Pin, GPIO_PIN_RESET);
	HAL_Delay(10);
	sendByte = 59 | (0x80);	// 59 to 64 contains accel data
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	retVal.x = (rcvByte) << 8;
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	retVal.x |= (rcvByte);
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	retVal.y = (rcvByte) << 8;
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	retVal.y |= (rcvByte);
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	retVal.z = (rcvByte) << 8;
	HAL_SPI_TransmitReceive(&hspi3, &sendByte, &rcvByte, 1, HAL_MAX_DELAY);
	retVal.z |= (rcvByte);
	HAL_GPIO_WritePin(MPU9250_CS_GPIO_Port, MPU9250_CS_Pin, GPIO_PIN_SET);
	return retVal;
}

